				<h1>A patient&#39;s guide to having a PET-CT scan</h1>
				<p>
					A brief guide to a PET&#45;CT scan as offered by the PET Imaging Centre at St Thomas&#39; Hospital and the Cancer Centre at Guy's is now available. This leaflet is for our patients and can either be downloaded directly from this site or a copy can be requested by contacting the	department. Information from the leaflet is also included below in our FAQ.
				</p>
        <p>
          <a href="./documents/PETCT_leaflet.pdf">Click here to download the guide.</a>
        </p>

        <hr>

				<h1>What is a PET-CT scan?</h1>
				<p>
					PET-CT &#40;positron emission tomography coupled with computed tomography&#41;, is a safe and easy scanning method. It shows us how the tissues in the body are functioning and helps us to identify organs or tissues that are not working normally. With PET-CT, we inject a tracer &#40;often a weak radioactive form of sugar&#41; then scan the body. The tracer shows up on the scan. Using this method we can get important information about many conditions affecting different organs of the body, which will help your doctor to plan appropriate treatment for you.
				</p>

        <hr>

				<h1>Do I need to confirm my appointment?</h1>
				<p>
					Yes. It is very important that you call us to confirm your appointment. It is also important that you allow plenty of time to get to the PET Centre, as scans generally cannot be delayed.
				</p>
				<p>
					You must tell us in advance if you know you are pregnant &#40;or think that you may be&#41;, are breastfeeding, or are the
					main carer of small children. We may need to give you additional information.
				</p>

        <hr>

				<h1>Can I eat or drink before my scan?</h1>
				<p>
					You will be asked not to eat and to drink plain water only. Please check your appointment letter carefully as it
					contains important information about not eating before your scan. Please note that that you do <b>not</b> need to keep a full bladder.
				</p>
				<p>
					If you have diabetes, please contact us on 020 7188 4988 for further information.
				</p>

        <hr>

				<h1>How long will the scan take?</h1>
				<p>
					This will depend on the type of scan, but you should expect to be in the department for two to three hours. Typically you will spend about 30 minutes in the scanner.
				</p>

        <hr>

				<h1>Can I bring someone with me?</h1>
				<p>
					If you wish, a relative or friend may accompany you and wait in the waiting room. It is very important that you do not bring children or pregnant women to accompany you in the department.
				</p>

        <hr>

				<h1>What should I wear?</h1>
				<p>
					We suggest that you wear something warm, loose and comfortable. As part of the examination includes a CT scan, it is important that you are not wearing any metal &#45; including jewellery, watches, zips, and bra hooks &#45; during the scan, as this may interfere with the scan. A hospital gown will be provided.
				</p>

        <hr>

				<h1>What will happen to me during the scan?</h1>
				<p>
					This will depend on the type of scan, and will be fully explained to you by our staff when you arrive. We will insert a small needle into a vein in your arm, hand, or occasionally foot &#40;if we cannot use an arm vein&#41;. We then inject the tracer. If you are having a brain or body scan we will ask you to relax quietly for 30 &#45; 90 minutes before we take you to the scanner room.
				</p>
				<p>
					We will ask you to lie on the scanner bed where we will make you as comfortable as possible. You will need to keep very still during the scan. It is important that you do not move, or the pictures could be blurred and the scan will have to be repeated. Once you are positioned correctly, we will move you into the scanner. The scanner bed will then move gently and slide into the centre of the scanner until the part of the body to be scanned is correctly positioned.
				</p>

        <hr>

				<h1>Will I feel anything?</h1>
				<p>
					The scan should be completely painless. The most difficult part is keeping still. Make sure you are as comfortable as possible before we start and try to relax.
				</p>

        <hr>

				<h1>Can I bring something to pass the time?</h1>
				<p>
					You will be able to read or listen to music while you are relaxing quietly before your scan &#40;unless you are having a brain scan&#41;. Once you are in the scanner we can play music for you via the internet.
				</p>
				<p>
					Please note that, for your safety, we have CCTV monitoring in the patient areas as staff cannot be present at all times in all areas.
				</p>

        <hr>

        <h1>What happens after the scan?</h1>
        <p>
          As soon as the scan is finished, you can go home, or back to your ward if you are staying in the hospital. You can eat and drink as normal and resume your usual activities. We normally supply a sandwich to eat after the scan.
        </p>
        <p>
          The pictures taken during the scan are carefully studied by the imaging doctor who will produce a detailed report.
        </p>

        <hr>

				<h1>Are there any risks?</h1>
				<p>
					PET-CT scans use a small amount of radiation to form images of your body and provide your doctor with clinical information on both the structure and function of your body. Any exposure to radiation carries a very small risk of potential tissue damage that could lead to cancer at a later date, but the doctor reviewing your case has decided that the potential benefits of the scan outweigh the small risk due to the radiation exposure.
				</p>

        <hr>

				<h1>How much radiation is the average person normally exposed to?</h1>
				<p>
					We are constantly exposed to radiation from a number of sources, including radioactive materials in our environment, radon gas in our homes, and cosmic rays from outer space. This is called background radiation and it varies depending on where you live. In the UK, the average person is exposed to about 2.2mSv (millisieverts) of background radiation from natural sources over 1 year.
				</p>

        <hr>

				<h1>How much radiation exposure is a PET-CT scan?</h1>
				<p>
					The amount of radiation you're exposed to in a standard PET-CT scan is small. The dose you will receive depends on several factors but as a guide for an average-sized adult:
				</p>
				<ul>
					<li><b>PET-CT brain scan:</b> Up to 5mSv per scan. This is about the same amount you get from natural background radiation over 2 years.</li>
					<li><b>PET-CT body or heart scan:</b> Up to 13mSv per scan. This is about the same amount you get from natural background radiation over 6 years.</li>
				</ul>

        <hr>

				<h1>What side-effects are there?</h1>
				<p>
					There are no side effects from the radioactive tracer. Occasionally when performing body or heart scans we may need to give you other drugs and, if this is the case, we will discuss the possible minor side effects with you.
				</p>

        <hr>

				<h1>When will my results be available?</h1>
				<p>
					We will send the results to the doctor who requested the scan, usually within 48 hours of the scan being completed. If results are needed more urgently your doctor can ask us to email or fax them. If you have a further appointment with your doctor please let us know the date when you attend for your scan.
				</p>
