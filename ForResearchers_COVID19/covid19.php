          Research at the PET Centre: COVID-19 Pandemic
        </div>

        <div class="covid">
          <p>
            Due to the COVID-19 pandemic we are having to make significant changes to our services. All non-essential research has been paused by the University with effect from 23 March 2020. The exception are COVID-19 studies and research where discontinuation would have “significant detrimental effects on the ongoing care of individual participants involved in those studies” (see below); in practice these are studies where PET-CT is part of the standard of care.
          </p>
          <p><strong>You may have to notify entities like sponsors, R&D, IRAS or funders of your studies being paused; see below for details.</strong></p>
          <p>
            The full R&D guidance as of 18 March 2020 is reproduced below:
          </p>
          <em><p>
            To support the Trust’s response to the COVID-19 situation, we will be implementing the following in relation to patient recruitment into existing research studies and new research studies, including clinical trials. The requirements set out below are in line with the NIHR’s guidance on research which was issued this week (see below) and apply to both commercial and non-commercial research studies with immediate effect.
          </p>
          <ul>
            <li>No new research studies will be opened unless they are COVID-19 research studies;</li>
            <li>New patient recruitment for all studies will be paused unless there are exceptional reasons as to why recruitment of new patients is vital. If Investigators consider that they have exceptional circumstances for continuing to recruit to any study, this needs to be endorsed by the Clinical Director, R&D Lead and approved by the R&D Department via Kate Blake and Charles Wolfe.</li>
            <li>Investigators need to identify how patients in their research studies should continue to participate, especially if discontinuing them will have significant detrimental effects on the ongoing care of individual participants involved in those studies. To do this, investigators may need to look at alternative ways of delivering these studies, which would lead to protocol amendments and deviations. Please note how to report and document these below. Investigators would also need to ensure that they have contingency and continuity plans in place if research staff become unavailable due to sickness or redeployment.</li>
          </ul>
          <h1>Amendments and protocol deviations for studies / trials currently running</h1>
          <h2>Amendments / Urgent Safety Measure</h2>
          <ul>
            <li>If you are notified of a new protocol amendment by an external trial sponsor, please forward this to <a href="mailto:r&d@gstt.nhs.uk">r&d@gstt.nhs.uk</a> or to <a href="mailto:KHPCTOamendments@kcl.ac.uk">KHPCTOamendments@kcl.ac.uk</a>.</li>
            <li>The R&D Governance Team or the KHP-CTO Commercial team will process the amendment and inform you when the amendment is approved / provide acknowledgment.</li>
            <li>If you wish to introduce a protocol amendment or Urgent Safety Measure for a GSTT/KCL sponsored CTIMP please contact <a href="mailto:khpcto.crateam@kcl.ac.uk">khpcto.crateam@kcl.ac.uk</a>. For non-CTIMPs please contact <a href="mailto:r&d@gstt.nhs.uk">khpcto.crateam@kcl.ac.uk</a>.</li>
          </ul>
          <h2>Protocol Deviations</h2>
          <ul>
            <li>In light of the changes the delivery of research studies, there will be an increase in protocol deviations. Please ensure that any deviations from the protocol is well documented, to enable appropriate evaluation for the trial at a later date.</li>
            <li>Please note that an increase in protocol deviations in relation to Coronavirus (COVID-19) will not constitute a serious breach, therefore, there is no need to report this to the MHRA, unless patients are being put at risk. Please see Note 1 attached for further information.</li>
          </ul>
          <p>
            Please continue to review the <a href="https://www.hra.nhs.uk/planning-and-improving-research/policies-standards-legislation/covid-19-guidance-sponsors-sites-and-researchers/">HRA website</a> for any study changes in relation to COVID-19 as the guidance is updated on a regular basis. Please note that in line with Trust policy R&D Governance Team and KHP-CTO will continue to acknowledge Cat C amendments.
          </p>
          <h1>Latest guidance on NIHR funded or supported research</h1>
          <p>
            The Department of Health and Social Care have also issued guidance on the impact of COVID-19 on NIHR funded or supported research. Key messages from the guidance are:
          </p>
          <p>
            Given the scale of the national and global challenge of COVID-19, clinical and academic health and care professionals currently funded by NIHR, who are working on topics other than COVID-19, should prioritise frontline care where requested to do so by their employing organisations.
          </p>
          <p>
            Clinical trials or other research studies that are funded or supported by NIHR should continue if discontinuing them will have significant detrimental effects on the ongoing care of individual participants involved in those studies.
          </p>
          <p>
            The full guidance is available <a href="https://www.nihr.ac.uk/news/dhsc-issues-guidance-on-the-impact-on-covid-19-on-research-funded-or-supported-by-nihr/24469">here</a>.
