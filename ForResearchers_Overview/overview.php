        <p>
          A major investment in the PET Centre in 2013 allowed the acquisition of two new PET-CT scanners and a PET-MRI scanner as well as a new radiochemistry facility and a second cyclotron. In 2016 we opened a new PET-CT Suite within the new Cancer Centre at Guy&#39;s.
        </p>

        <hr>

				<h1>Particular Interests</h1>
				<p>
					The PET centre provides a stable infrastructure with a busy imaging service to underpin health economic, translational and basic science research for a number of research groups. The ongoing installation of a new on-site radiochemistry facility with 24 hot cells and a new cyclotron supports our goal to research non-FDG PET tracers, translating novel tracers, and using previous tracers in novel applications.
				</p>
				<p>
          Areas of particular research interest within the Centre include monitoring and planning therapy. Members of the Department lead the UK <a href="http://www.ncri-pet.org.uk/" target="_blank">National Cancer Research Institute (NCRI) PET core lab</a> that has successfully co-ordinated a number of national and international multicentre PET trials. We aim to continue to develop national clinical studies with a health economic element and to continue to develop and translate non-FDG tracers.
				</p>
				<p>
          In oncology, further current trials include the use of PET in planning radiotherapy, in imaging tumour hypoxia, and in monitoring therapy response in skeletal metastases on both PET-CT and PET-MRI platforms. A number of trials are running using PET methodology and tracers to monitor therapy response to novel therapeutics. There is also interest in developing novel methods of data analysis, exploring tumour heterogeneity and using bioinformatics in the field of radiomics.
				</p>
        <p>
					Research in neurology and neuropsychiatry is increasing. For example, ongoing studies explore cerebral function in obesity; aim to rationalise antipsychotic drug treatment; evaluate the role of <sup>11</sup>C-methionine in the diagnosis of peripheral nerve sheath tumours; or evaluate brain amyloid in twins discordant for cognitive impairment. We are also involved in the development of tracers for novel targets, with studies currently ongoing in schizophrenia, autism, and neurocognitive impairment/dementia.
				</p>
				<p>
					In cardiology there is interest in measuring blood flow and metabolism with a variety of tracers.
				</p>

